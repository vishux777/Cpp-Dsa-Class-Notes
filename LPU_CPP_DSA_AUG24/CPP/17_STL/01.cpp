/*
STL -> Standard Template Library

/STL has 3 major components -> 
    *1. Containers -> they store and organize data in a specific manner
        - Sequence containers -> Array, Vectors, Queue, Deque, List -> they are implemented as arrays or linkedlist.
        - Associative containers -> Set, Multiset, Map, Multimap they are implemented as Binary Tree internally
        - Unordered Associative containers -> Unordered_set, unordered_multiset, unordered_map, unoedered_multimap -> internally they are implemented as hash table


    *2. Iterators -> these are the objects that are used to access elements of a container
    
    *3. Algorithms -> Sorting, searching, copy, count, swap etc. 

*/ 

